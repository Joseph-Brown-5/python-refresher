# Goal: Create a program that takes input from the user and calculates the area of a rectangle

# get user input (width and length)
width = float(input("What is the width?: "))
length = float(input("What is the length?: "))

# do the appropriate opperations
area = width * length

# print the area to the console
print(f"The area of a rectangle with sides {width} and {length} is: {area}")
