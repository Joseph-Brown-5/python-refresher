course = "Python Programming"

# get the length of the string
print(len(course))

# get the first character of the string
print(course[0])

# get the last character of the string
print(course[-1])

# get a substring of the string
print(course[0:3])

# get a substring from the string starting at index 0 to the end
print(course[0:])

# get a substring from the string starting at the beginning up to index 3
print(course[:3])

# get a substring of the string starting at the beginning up to the end
print(course[:])
